#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--) {
        int N;
        cin >> N;
        vector<long long> A(N);
        for (int i = 0; i < N; i++) cin >> A[i];

        long long d = A[1] - A[0];
        for (int i = 2; i < N; i++) {
            d = gcd(d, A[i] - A[i - 1]);
        }

        long long total_terms = (A[N - 1] - A[0]) / d + 1;
        long long operations = total_terms - N;

        cout << operations << '\n';
    }
    return 0;
}

