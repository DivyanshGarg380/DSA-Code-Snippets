#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--) {
        long long N, M;
        cin >> N >> M;
        if (M < N || M > 3 * N) {
            cout << "NO\n";
        } else if ((M - N) % 2 != 0) {
            cout << "NO\n";
        } else {
            long long k = (M - N) / 2;
            if (k >= 0 && k <= N) cout << "YES\n";
            else cout << "NO\n";
        }
    }
    return 0;
}

